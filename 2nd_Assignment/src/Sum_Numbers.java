// Take two Numbers and add them. Print the sum on the screen.


public class Sum_Numbers {
	public static void main(String [] args) {
		
//Sum of integer numbers
		int n= 48, p= 52 ;
		int a=n+p;
		System.out.println("Sum of two integer numbers is: " + a);
		
//Sum of float numbers
		float N= (float)48.2 ,P= (float)52.8;
		float A=N+P;
		System.out.println("Sum of two float numbers is: " + A);
		
//Sum of Double numbers
		double NN=52.4, PP=48.5;
		double AA=NN+PP;
		System.out.println("Sum of two double numbers is: " + AA);
		
//Sum of two short numbers
		short nn=54, pp=28;
		short aa=(short)(nn+pp);
		System.out.println("Sum of two short numbers is: " + aa);	
	} 
}
